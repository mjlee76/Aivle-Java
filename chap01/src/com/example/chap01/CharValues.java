package com.example.chap01;

public class CharValues {
    public static void main(String[] args) {
        char c = 'A'; // 65로 변환해서 c에 집어넣는다.
        char d = 65;
        char e = 66;
        char f = 97;

        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}
