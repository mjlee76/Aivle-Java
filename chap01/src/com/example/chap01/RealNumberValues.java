package com.example.chap01;

public class RealNumberValues {
    public static void main(String[] args) {
        float a = 100;
        double b = 101;

        System.out.println("float a의 값 : " + a);
        System.out.println("double b의 값 : " + b);

        float A = Float.MAX_VALUE;
        System.out.println("float A의 최대값 : " + A);

        System.out.println("double 의 최대값 : " + Double.MAX_VALUE);  // 직접 넣어도 가능
    }
}
